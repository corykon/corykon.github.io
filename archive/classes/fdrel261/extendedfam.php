        <h3>Memories with Extended Family</h3>
        <p>We were also pretty close to our cousins on my mother's side. Basically from the time that my parents moved to the east coast until later in high school, we would drive up to my grandparent's home in Whitehall, New York at least a couple times a year for Thanksgiving and over the summer. Most of that side of the family either lived somewhere nearby or would visit during those times as well, so there were about a dozen of us cousins that became a tight-knit little group. Those little reunions created some of my favorite memories as a child.
        </p><p>A couple of my cousins were around the same age, Jimmer and John, and so we became great friends, and have been around each other for as long as I can remember. I'm older by them by a year, but I've always been a smaller kid so it wasn't long before they outgrew me.
        </p>
