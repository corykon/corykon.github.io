        <img src="images/cousins5.jpg" alt="Cory and Jimmer">
        <p>I remember that we had tons of yearly traditions and events led by our older cousins TJ, Mike, and Lindsay, who are at least 6 or so years older then I was. We had this little club that they created, where we would graduate to different groups or levels as we got older, and each year we would have what we called the “Loke” ceremony. I can't remember all that went along with it, but to us it was epic and so much fun. I remember that TJ would always tell stories; scary stories, funny stories, adventure stories, and all kinds of stuff that he would just make up. He created these story lines and mythologies that we'd come back to each year and he'd elaborate on them. All of us younger kids were completely enthralled.
        </p><p>My grandparents had a huge in-ground pool in the back with a diving board and a basketball setup, and so some of our favorite games revolved around that. We'd have competitions off the diving board to see who could do the funniest dive, the biggest splash, the furthest jump, the best Michael Jackson impersonation, or whatever else we could think of. Whoever won would be the next judge, and then he or she would pick the next categories.
        </p>
