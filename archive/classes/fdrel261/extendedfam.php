        </p><p>Grandma would always put together big trays of snacks and bring them out to us by the pool, so we'd take breaks and then gather around to gobble them up. I remember that in general there was always tons of food when the family would get together, and a lot more sweets and junk food than we'd get at home, so that was just one more thing to love about our visits to New York.
