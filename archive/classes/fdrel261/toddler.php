        <h3>As a Toddler</h3>
        <p>The fact that I was such a “chill” baby is probably why I was so old before I started walking. It wasn't until I reached 17 months that I learned. There was one night where my parents finally decided, “Alrighty, its time for this kid to start walking.” As my mother described, “We worked and worked, and in one evening you learned to walk... you were so proud of yourself. You would walk in a big circle of the living room doing air fist pumps as only a 17 month old could so with a huge smile on your face. We all were proud and relieved.”
        </p>
        <p>When I was little I had lots of different toys than my sister and I would play with, but there wasn't one in particular that ever stood out as my favorite. I did have this green blanket that I was very attached to, and so my mother bought two of them so that a replacement was ready when the first become too raggedy and dirty. I also always liked playing with matchbox cars, and my mother remembers playing with cars as one of the things she could do to really have fun with me. I was a very somber child so it would take some extra effort to get me laughing, but she discovered for some reason that by “vrooming” the cars extra fast and crashing them into the wall heater I would absolutely crack up and let out this uncontrollable laughter. She would do it over and over again until we both became bored with it.
        </p>
        <p>Around the age of 18 months I became quite the momma's boy. I had hard time being away from mom, and would refuse to be held by or left alone with strangers. 
        </p>
        <img src="images/child5.jpg" alt="Mamma's Boy">
        <img src="images/child6santa.jpg" alt="Scary Santa">
        <p>I remember being left to be babysat by someone for an hour or so, I think it was a sister from our ward, and I just cried and cried as soon as mom left. They tried everything they could to calm me down, but I wouldn't have it. They brought out a bunch of toy cars, and the son who was a few years older tried to get me to play with them, but I refused. Finally after a half an hour or longer I calmed down and started playing, but almost immediately mom was back.
        </p>
        <p>Staying in the comfort zone has been an ongoing theme for me. I always get caught up in a routine, a circle of things that I'm accustomed to and familiar with, and can have a had time breaking away from those things. It would be a struggle when most anything would change in my life. Even the small things, like buying new shoes, would become an epic battle. We'd go to the store and I would scream and cry as my parents tried to find a pair that would fit. As soon as a pair was found, they would take them off and put the old ones back on, and then the new shoes had to be slowly transitioned into my life. Another example would be my picky eating habits. As an infant I liked milk, baby apple food, and not much else. Luckily as I got older and peanut butter and cheerios become options, so my menu of acceptable items began to expand, but it was a long and slow process.
        </p>
        <p>My mother's recalls once during a visit to our grandparents in New York where she came to me to ask for hug. I responded cooly, “Maybe tomorrow.” I had started growing out of my momma's boy phase, and wouldn't accept the hugs as readily, but I would put on quite the show if a lady was in the room. I would strut around with a bit of a sniffle and my chest puffed up, trying to pretend I wasn't paying attention, but at the same time soaking in all the attention I could grab.  Another funny trait came about after I had learned how to talk. There were a few words that I had trouble with and would always mix up, like “peek” and “keep.” If I found a bug or something I would fervently beg, “Can I peek it?” or I would sneak around a corner with my hands circled around my eyes like binoculars and say “I'm keeping at you!” I also had trouble with the word “diary”; somehow I would throw in a couple extra syllables and it would come out as “diarrhea.” Another that I seemed to struggled with was the phrase “You're under arrest!” Sometimes in games I would be the cop, and would chase the bad guy around yelling “You're under the rest!” My parents would correct me, but it was the longest time before I could understand the difference.
        </p>
            
